export const formatDate = (dateString: string): string => {
  const date = new Date(dateString)
  return date.toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  })
}

export const getTagLabel = (tag: string): string => {
  return tag.charAt(0).toUpperCase() + tag.slice(1)
}
